package uk.co.norphos.crystallography.api;

/**
 * An enum used to indicate the highest symmetry direction of a 
 * {@link Lattice}. This is particularly useful/important with monoclinic 
 * crystal systems. 
 * 
 * @author Michael Wharmby
 *
 */
public enum PrincipleAxis {
	A, B, C, NONE;

}
